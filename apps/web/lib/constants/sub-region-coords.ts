/**
 * 좌표 조회 — 정확 매칭 → 부분 매칭 (예: '인제군' → '인제') 순서로.
 */
export function lookupSubRegion(name: string): { lat: number; lng: number } | null {
  if (!name) return null
  if (SUB_REGION_COORDS[name]) return SUB_REGION_COORDS[name]
  // 부분 매칭 — '인제군청' / '강릉시' 같은 변형 흡수
  const stripped = name.replace(/(시|군|구|읍|면|동|리)$/g, '').trim()
  if (stripped && SUB_REGION_COORDS[stripped]) return SUB_REGION_COORDS[stripped]
  // 키 안에 name 이 부분 포함된 첫 항목 (예: '주문진' 매칭)
  for (const key of Object.keys(SUB_REGION_COORDS)) {
    if (key.includes(name) || name.includes(key)) return SUB_REGION_COORDS[key]
  }
  return null
}
